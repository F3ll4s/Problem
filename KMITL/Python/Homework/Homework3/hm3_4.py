from turtle import *

radius = int(input("Enter the radius of the ring: "))

color("blue")
pensize(5)
penup()
goto(-radius*2.4,0)
pendown()
circle(radius)
color("black")
pensize(5)
penup()
goto(0,0)
pendown()
circle(radius)
color("red")
pensize(5)
penup()
goto(radius*2.4,0)
pendown()
circle(radius)
color("yellow")
pensize(5)
penup()
goto(-radius*2.4/2,-radius)
pendown()
circle(radius)
color("green")
pensize(5)
penup()
goto(radius*2.4/2,-radius)
pendown()
circle(radius)

exitonclick()
